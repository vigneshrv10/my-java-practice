public class TypeCasting {
    public static void main(String[] args) {
        // float a = 25.12f;
        char ch = 'a';
        char ch2 = 'b';
        System.out.println(ch2-ch);
        int n1 = ch;
        int n2 = ch2;
        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n2-n1);
    }
    
}
