public class RelationalOperator {
    public static void main(String[] args) {
        System.out.println("Relational operators are ==,!=,>,<,>=,<=");
        int a = 10;
        int b = 5;
        System.out.println(a==b); //equal
        System.out.println(a!=b); // not equal
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
    
}
