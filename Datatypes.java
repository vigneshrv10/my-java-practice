public class Datatypes {
    public static void main(String[] args) {
        System.out.println("There are different data types in java. They are primitive(already exisiting) and non_primitive");
        System.out.print("Primitive(bytes): byte - 1,short - 2,char - 2,boolean - 1,int - 4,long - 8,float - 4,double - 8\n");
        System.out.print("Non primitive: String, Array, Class, Object, Interface\n");
        System.out.println("1 byte = 8 bits");
        byte b = 8;
        System.out.println("byte: " + b);
        char ch = 'a';
        System.out.println("char: " + ch);
        boolean var = true;
        System.out.println("boolean: " + var);
        float price = 10.5f;
        System.out.println("float: " + price);
        int num = 5;
        System.out.println("int: " + num);
        long num1 = 10000000000L;
        System.out.println("long: " + num1);
        double num2 = 100.5;
        System.out.println("double: " + num2);
        String str = "Hello World";
        System.out.println("String: " + str);
        
    }
    
}
